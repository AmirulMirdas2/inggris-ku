export type PixelIconName = keyof typeof SPRITES

export function PixelIcon({ name, size = 24, className = '' }: {
  name: PixelIconName
  size?: number
  className?: string
}) {
  const s = SPRITES[name]
  return (
    <svg
      viewBox="0 0 8 8" width={size} height={size} shapeRendering="crispEdges"
      className={className} aria-hidden focusable="false"
    >
      {s.rows.map((row, y) =>
        [...row].map((ch, x) =>
          ch === '.' ? null : <rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" fill={s.c[+ch]} />,
        ),
      )}
    </svg>
  )
}
